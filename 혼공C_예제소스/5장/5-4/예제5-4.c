#include <stdio.h>

int main(void)
{
	int a = 20, b = 10;

	if (a > 10)        // a가 10보다 크면 9 ~ 16행 실행, 작거나 같으면 18행으로 이동
	{
		if (b >= 0)    // b가 0 이상이면 b에 1 대입하고 18행으로 이동
		{
			b = 1;
		}
		else
		{
			b = -1;    // b가 0보다 작으면 b에 -1 대입하고 18행으로 이동
		}
	}

	printf("a : %d, b : %d\n", a, b);

	return 0;
}

// if in if  
// 1. a에 20, b에 10을 대입 
// 2. 1조건으로 a가 10보다 큰 경우
// 3. 1-1조건으로 a가 10보다 크고 b는 0과 같거나 큰 경우 : b에 1대입 
// 4. 1-2조건으로 a가 10보다 큰데 b는 0보다 작은 경우(위 조건이 아닌 경우) : b에 -1 대입 
// 5. 해당에 맞는 조건 출력은 a : 20, b : 1 출력 